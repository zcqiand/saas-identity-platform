def main() -> None:
    # 1. 子项数据表 (供后续步骤复用)
    map_data = {
        "schema": 1,
        "generated_by": "generate_proposal.py",
        "total": len(SUBITEMS),
        "items": [
            {"id": i, "name": n, "type": t, "status": s}
            for (i, n, t, s) in SUBITEMS
        ],
    }
    (STATE / "subitem_map.json").write_text(
        json.dumps(map_data, ensure_ascii=False, indent=2) + "\n", encoding="utf-8"
    )

    # 2. tree-change 提案 JSON
    # 父级 F 都已存在, M06.F08 需声明为新增功能(其父模块 M06 已存在)
    # M07 占位模块本提案不新增(Dashboard 无测试、无强制要求) — 留作未来
    proposal = {
        "schema": 1,
        "approved": False,
        "base_sha": "",
        "summary": (
            "把功能树从 feature(F)级拆到子项(I)级 — 新增 133 个 I 级子项, 覆盖全部 26 个功能"
            "(M01-M06); 新增 M06.F08 平台基础配置 收纳悬空 PlatformConfig 页面。"
            "所有新子项初始标「开发中」; 后续在 src 补 data-fn 锚点 + tests 挂 fnTest ID 后统一转「已上线」。"
        ),
        "changes": [
            {"id": fid, "action": "新增", "name": n, "reason": "拆分 F 级到 I 级, 粒度=一权限点"}
            for (fid, n, _t, _s) in SUBITEMS
        ] + [
            {"id": "M06.F08", "action": "新增", "name": "平台基础配置",
             "reason": "收纳悬空页 src/pages/PlatformConfig.tsx + 3 个测试"},
        ],
        "sync_files": [],
    }
    (STATE / "tree-change.json").write_text(
        json.dumps(proposal, ensure_ascii=False, indent=2) + "\n", encoding="utf-8"
    )

    print(f"子项数: {len(SUBITEMS)}")
    print(f"提案 changes 总数: {len(proposal['changes'])}")
    print(f"输出: {STATE/'tree-change.json'}, {STATE/'subitem_map.json'}")


if __name__ == "__main__":
    main()
